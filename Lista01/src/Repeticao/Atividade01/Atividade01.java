package Repeticao.Atividade01;

import javax.swing.JOptionPane;

/* 1.	Exibir os números de 1 até 50 na tela.  */

public class Atividade01 {

    public static void main(String[] args) {

        for (int i = 1; i <= 50; i++) {
            JOptionPane.showMessageDialog(null, i);
        }

    }

}